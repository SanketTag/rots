package com.rots.contract;

public class KeyValueWrapper {

	private String key;
	private Double Value;
	
	public String getKey() {
		return key;
	}
	public void setKey(String key) {
		this.key = key;
	}
	public Double getValue() {
		return Value;
	}
	public void setValue(Double value) {
		Value = value;
	}
	
	
}
